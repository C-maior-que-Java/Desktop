    public TelaFazerPedido(TelaPrincipal telaPrincipal) {
        initComponents();
        this.telaPrincipal = telaPrincipal;
        
        pedido = new Pedido();
        jLabel4.setText(String.valueOf(pedido.getNumPedido()));
        jLabel6.setText("R$ 0,00");
        
        cardapioTableModel = (DefaultTableModel) jTable1.getModel();
        pedidoTableModel = (DefaultTableModel) jTable2.getModel();

        this.setDefaultCloseOperation(DISPOSE_ON_CLOSE);

        
     
        Cardapio cardapio = telaPrincipal.getCardapio();
        produtosNoCardapio = cardapio.getProdutos();
        
        for(Produto produto : produtosNoCardapio) {
        
            adicionarLinhaTabela(produto, cardapioTableModel);
        
        }
        
        pedidoTableModel.addTableModelListener(new TableModelListener() {

        @Override
        public void tableChanged(TableModelEvent e) {
            if (e.getType()==TableModelEvent.INSERT||e.getType()==TableModelEvent.DELETE) {
                
                jLabel6.setText(df.format(pedido.getValorPedido()));
            }
        }
    });
    }

    private void adicionarLinhaTabela(Produto produto, DefaultTableModel model) {
  
        model.addRow(new Object[]{String.valueOf(produto.getID()), df.format(produto.getPreco())});
        
    }
