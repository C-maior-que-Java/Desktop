    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        int indice = jTable2.getSelectedRow();
        
        if(indice >= 0) {
            
            Produto produto = pedido.getProdutos().get(indice);
            pedido.removerProduto(produto);
            pedidoTableModel.removeRow(indice);
        }

        
       
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        Cliente c = telaPrincipal.buscarCliente(txtNomeCliente.getText());
        pedido.setCliente(c);
        if(cbxGerarEntrega.isSelected())
            pedido.setRealizarEntrega(true);
        telaPrincipal.adicionarPedidoNaLista(pedido);
        this.dispose();
        
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        int indice = jTable1.getSelectedRow();
        
       
        if(indice >= 0) {
        
            Produto novoProduto = produtosNoCardapio.get(indice);        
            pedido.adicionarProduto(novoProduto);
            adicionarLinhaTabela(novoProduto, pedidoTableModel);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

    

